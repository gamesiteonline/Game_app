// Function to get all games (including those not manually enriched)
export const getAllPSPGames = (rawList: {game_name: string, url: string}[]): PSPGame[] => {
  return rawList.map(raw => {
    const enriched = PSP_GAMES.find(g => g.name === raw.game_name.replace(".cso", "").replace(".7z", ""));
    if (enriched) return enriched;
    
    // Default values for games not manually enriched
    return {
      name: raw.game_name.replace(".cso", "").replace(".7z", ""),
      url: raw.url,
      genre: "PSP Classic",
      size: "Varies",
      rating: 4.0
    };
  });
};
